// sample1-3.cpp: 文字列の出力(3)
#include <iostream>		// 標準入出力ライブラリを読み込む
using namespace std;		// std という名前空間を使うことを宣言

int main (void) {		// main 関数の始まり
  cout << "私の名前は"		// 自分の名前を表示
       << "川内 情太郎"
       << "です" << endl;
  cout << "私は"		// 自分の誕生日を表示
       << 1998 << "年"
       << 4 << "月"
       << 31 << "日"
       << "生まれです" << endl;
  return 0;
}
